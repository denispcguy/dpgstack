def icon_for_model(model_name: str) -> str:
    """Return an SVG path string for the given model name.

    The icon is selected deterministically by hashing the model name,
    so the same model always gets the same icon.
    """
    digest = hashlib.sha256(model_name.encode()).hexdigest()
    index = int(digest, 16) % len(_ICONS)
    return _ICONS[index]